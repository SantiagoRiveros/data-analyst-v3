# Operadores Logicos

# AND
True and True # True
True and False # False
False and False # False
False and True # False

edad = 19
peso = 49

if edad > 18 and peso < 50:
    print("Hola")

# OR
True or True # True
True or False # True
False or True # True
False or False # False

edad2 = 20
peso2 = 60

if edad2 > 50 or peso2 < 100:
    print("Hola2")


# NOT

not True # False
not False # True

# and -> Y
# or -> O
# NOT -> NO